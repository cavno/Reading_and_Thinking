function getChapter(id) {
  const key = chapters[id] ? id : 'prologue';
  return Object.assign({ id: key }, chapters[key]);
}

function getNav(id) {
  const i = order.indexOf(id);
  const at = i < 0 ? 0 : i;
  return {
    prev: at > 0 ? { id: order[at - 1], title: chapters[order[at - 1]].navTitle } : null,
    next: at < order.length - 1 ? { id: order[at + 1], title: chapters[order[at + 1]].navTitle } : null
  };
}

// 首页目录（脉络）数据
const toc = order.map(function (id) {
  const c = chapters[id];
  return { id: id, roman: c.roman, romanLatin: c.romanLatin, title: c.navTitle, subtitle: c.subtitle };
});

module.exports = { chapters, order, toc, getChapter, getNav };
